<h1 class="text-xl leading-tight font-semibold">Adequate</h1>
<p class="my-4 text-sm leading-relaxed text-neutral-700">
	Adequate means "sufficient" or "acceptable" in quality or quantity. It indicates that something
	meets the necessary requirements or standards, but does not exceed them. For example, an adequate
	performance is one that fulfills the basic expectations without being exceptional.
</p>
